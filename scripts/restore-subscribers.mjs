async function main() {
  console.log('구독자 데이터 복원을 시작합니다...')
  
  for (const subscriber of subscribers) {
    try {
      await prisma.newsSubscriber.create({
        data: {
          ...subscriber,
          phone: subscriber.phone || ''  // 전화번호가 없는 경우 빈 문자열로
        }
      })
      console.log(`✅ 추가됨: ${subscriber.name} (${subscriber.email})`)
    } catch (error) {
      if (error instanceof Prisma.PrismaClientKnownRequestError && error.code === 'P2002') {
        console.log(`⚠️ 중복된 이메일: ${subscriber.name} (${subscriber.email})`)
      } else {
        console.error(`❌ 오류 발생: ${subscriber.name} (${subscriber.email})`, error)
      }
    }
  }
  
  console.log('구독자 데이터 복원이 완료되었습니다.')
}

